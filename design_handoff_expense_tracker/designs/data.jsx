function buildSeed() {
  const now = Date.now();
  const dayMs = 86400000;
  return SEED.map((row, i) => {
    const [name, category, price, daysAgo, hour] = row;
    const d = new Date(now - daysAgo * dayMs);
    d.setHours(hour, (i * 13) % 60, 0, 0);
    return {
      id: 'e' + i + '-' + d.getTime(),
      name,
      category,
      price,
      ts: d.getTime(),
    };
  }).sort((a, b) => b.ts - a.ts);
}

